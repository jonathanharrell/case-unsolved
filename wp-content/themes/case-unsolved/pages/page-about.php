    <h2>Writer/Director's Statement</h2>
    <p>
        ​As someone who got spooked by so many nonfiction ghost documentaries as a child, I look forward to pouring so many of my joys and anxieties about life, love, and nostalgia into CASE UNSOLVED. For myself and many other millennials, the mere mention of the classic real-life show Unsolved Mysteries elicits excited responses like, “That show scared me so much!” and “It was so cheesy!” and “I still remember the music!”  As a fanatic of scary stories in any format, I want to create an unlikely valentine not just to one specific show from my childhood, but to ‘80s & ‘90s horror at large. I want to explore the idea of translating horror aesthetics from decades past into today’s more thoughtful, artistic, and disturbing genre trends.
    </p>
    <p>
        My goal for CASE UNSOLVED is to showcase a heroic gay duo that viewers can celebrate with as much geeky love as they have for hetero heroes like Mulder and Scully, Doctor Who, and Buffy. Though certain viewers might not even be familiar with all these spooky artifacts from pop culture, I know this premise will resonate.  More than anything, I want this story to explore the anxious feeling of a “case unsolved” that exists in all of us, especially in this current moment in history. Even for those of us thriving in life, like Owen and Riz are, there is a very real and concrete fear that it all can be taken away at any second, and the intensity of that fear varies, depending on your mix of privileged and marginalized identities.  I want this project to explore how we crave resolutions in this age where there are no clean and happy endings.
    </p>
    <p>C.J. Arellano</p>
